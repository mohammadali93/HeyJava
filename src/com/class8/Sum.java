package com.class8;

public class Sum {
public static void main(String[] args) {
	
	int sum=0;
	for (int i=1; i<=5; i++) {
		sum=sum+i;
	}
	System.out.println(sum);
	
	// find sum of all even numbers from 1 to 100/
	
	int idk=0;
	for(int k=2; k<=100; k+=2) {
		idk=idk+k;
	}
	System.out.println(idk);
}
}
