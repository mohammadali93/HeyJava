package com.class2;

public class VariablesDeclaration {

	public static void main(String[] args) {

		// different ways to create/declare variable;
		// 1. create variable and assign value
		int num = 27;

		// 2 ways: using 2 steps;
		int y; // 1.declaring variable
		y = 12; // 2.assign value

		// declare 1 by 1
		int f;
		int g;
		int h;
		int k;
		int j;
		// or declare all together
		int r, x, s, b, o;

		System.out.println(y);
	}

}
